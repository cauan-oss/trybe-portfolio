-- para saber a media entre todos os registros usamos a funcao AVG;
select AVG(country_id) from sakila.city;

-- Para saber o menor valor encontrado entre todos os registros usamos a funco MIN;
select MIN(country_id) from sakila.city;

-- para saber o maior valor encontrado entre todos os registros usamos a funcao  MAX;
select MAX(country_id) from sakila.city;

-- para saber a soma dos valores encontrados entre todos os registros usamos a funcao  SUM;
select SUM(country_id) from sakila.city;

-- quantidade de registros encontrados 
select COUNT(country_id) from sakila.city;



